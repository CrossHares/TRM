package ldteam.dao;

public class LDTeam {

	String ldUserEmail;
	int ldVerRep;
	int urID;
	String ldUserName;
	String ldUserPhone;
	String ldUserPassword;
	
	public String getLdUserEmail() {
		return ldUserEmail;
	}
	public void setLdUserEmail(String ldUserEmail) {
		this.ldUserEmail = ldUserEmail;
	}
	public int getLdVerRep() {
		return ldVerRep;
	}
	public void setLdVerRep(int ldVerRep) {
		this.ldVerRep = ldVerRep;
	}
	public int getUrID() {
		return urID;
	}
	public void setUrID(int urID) {
		this.urID = urID;
	}
	public String getLdUserName() {
		return ldUserName;
	}
	public void setLdUserName(String ldUserName) {
		this.ldUserName = ldUserName;
	}
	public String getLdUserPhone() {
		return ldUserPhone;
	}
	public void setLdUserPhone(String ldUserPhone) {
		this.ldUserPhone = ldUserPhone;
	}
	public String getLdUserPassword() {
		return ldUserPassword;
	}
	public void setLdUserPassword(String ldUserPassword) {
		this.ldUserPassword = ldUserPassword;
	}
	
	
}
