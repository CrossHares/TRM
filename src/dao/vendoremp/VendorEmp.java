package dao.vendoremp;

public class VendorEmp {

	int vEmpId;
	int venId;
	String vEmpName;
	String vEmpEmail;
	String vEmpLocation;
	String vEmpAddress;
	String vEmpPhone;
	String vEmpSpecialty;
	
	
	
	public int getvEmpId() {
		return vEmpId;
	}
	public void setvEmpId(int vEmpId) {
		this.vEmpId = vEmpId;
	}
	public int getVenId() {
		return venId;
	}
	public void setVenId(int venId) {
		this.venId = venId;
	}
	public String getvEmpName() {
		return vEmpName;
	}
	public void setvEmpName(String vEmpName) {
		this.vEmpName = vEmpName;
	}
	public String getvEmpEmail() {
		return vEmpEmail;
	}
	public void setvEmpEmail(String vEmpEmail) {
		this.vEmpEmail = vEmpEmail;
	}
	public String getvEmpLocation() {
		return vEmpLocation;
	}
	public void setvEmpLocation(String vEmpLocation) {
		this.vEmpLocation = vEmpLocation;
	}
	public String getvEmpAddress() {
		return vEmpAddress;
	}
	public void setvEmpAddress(String vEmpAddress) {
		this.vEmpAddress = vEmpAddress;
	}
	public String getvEmpPhone() {
		return vEmpPhone;
	}
	public void setvEmpPhone(String vEmpPhone) {
		this.vEmpPhone = vEmpPhone;
	}
	public String getvEmpSpecialty() {
		return vEmpSpecialty;
	}
	public void setvEmpSpecialty(String vEmpSpecialty) {
		this.vEmpSpecialty = vEmpSpecialty;
	}
}
