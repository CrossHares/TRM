package dao.Login;

public class Requesterdao {
	
	String requester_id;
	String requester_name;
	String requester_email;
	String requester_vertical;
	
	


	public String getRequester_name() {
		return requester_name;
	}

	public void setRequester_name(String requester_name) {
		this.requester_name = requester_name;
	}

	public String getRequester_vertical() {
		return requester_vertical;
	}

	public void setRequester_vertical(String requester_vertical) {
		this.requester_vertical = requester_vertical;
	}

	public String getRequester_id() {
		return requester_id;
	}

	public void setRequester_id(String requester_id) {
		this.requester_id = requester_id;
	}

	public String getRequester_email() {
		return requester_email;
	}

	public void setRequester_email(String requester_email) {
		this.requester_email = requester_email;
	}
	
	

}
