package mypro;

public class DeveloperTrainingMember {
	private int dtmId;
	private String verId;
	private String dtmName;
	private String dtmPhone;
	private String dtmEmail;
	private String dtmEmployeeId;
	public int getDtmId() {
		return dtmId;
	}
	public void setDtmId(int dtmId) {
		this.dtmId = dtmId;
	}
	public String getVerId() {
		return verId;
	}
	public void setVerId(String verId) {
		this.verId = verId;
	}
	public String getDtmName() {
		return dtmName;
	}
	public void setDtmName(String dtmName) {
		this.dtmName = dtmName;
	}
	public String getDtmEmail() {
		return dtmEmail;
	}
	public void setDtmEmail(String dtmEmail) {
		this.dtmEmail = dtmEmail;
	}
	public String getDtmEmployeeId() {
		return dtmEmployeeId;
	}
	public void setDtmEmployeeId(String dtmEmployeeId) {
		this.dtmEmployeeId = dtmEmployeeId;
	}
	public String getDtmPhone() {
		return dtmPhone;
	}
	public void setDtmPhone(String dtmPhone) {
		this.dtmPhone = dtmPhone;
	}
}
